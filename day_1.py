# Operators and Datatypes
# Operators
print(2 + 4) # Addition Operator
print(2 - 4) # Subtraction Operator
print(2 * 4) # Multiplication Operator
print(2 / 4) # Division Operator
print(2 ** 4) # Exponent Operator
print(2 % 4) # Modulus Operator
print(2 // 4) # Floor Division Operator

#Type Function -- Data types
print(type(10)) #int
print(type(3.22)) #float
print(type(1+ 4j)) #complex
print(type('Python')) #str
print(type([1,2,3])) #list
print(type({'name': 'Who'})) #dictionary
print(type({9.8, 12.3, 22})) #set